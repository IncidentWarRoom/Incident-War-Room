// uniqueViolation is the PostgreSQL error code for unique constraint violations.
const uniqueViolation = "23505"

// IncidentRepository stores incidents in the "incidents" table.
type IncidentRepository struct {
	db Querier
}

func NewIncidentRepository(db Querier) *IncidentRepository {
	return &IncidentRepository{db: db}
}

// Create inserts a new incident. ID and CreatedAt are generated by the
// database and written back into inc.
//
// The "one active incident per chat" rule is enforced by the partial unique
// index idx_incidents_one_active_per_chat; its violation is translated
// into errs.ErrIncidentAlreadyActive.
func (r *IncidentRepository) Create(ctx context.Context, inc *incident.Incident) error {
	const query = `
		INSERT INTO incidents (title, severity, status, chat_id, topic_id, created_by)
		VALUES ($1, $2, $3, $4, $5, $6)
		RETURNING id, created_at`

	err := r.db.
		QueryRow(ctx, query, inc.Title, inc.Severity, inc.Status, inc.ChatID, inc.TopicID, inc.CreatedBy).
		Scan(&inc.ID, &inc.CreatedAt)
	if err != nil {
		var pgErr *pgconn.PgError
		if errors.As(err, &pgErr) && pgErr.Code == uniqueViolation {
			return errs.ErrIncidentAlreadyActive
		}
		return errs.Wrapf(errs.KindInternal, "repository.Incident.Create", err, "insert incident")
	}

	return nil
}

// GetByID returns the incident with the given ID,
// or errs.ErrIncidentNotFound if it does not exist.
func (r *IncidentRepository) GetByID(ctx context.Context, id uuid.UUID) (*incident.Incident, error) {
	const op = "repository.Incident.GetByID"
	const query = `
		SELECT id, title, severity, status, chat_id, topic_id, created_by, created_at, closed_at
		FROM incidents
		WHERE id = $1`

	inc, err := scanIncident(r.db.QueryRow(ctx, query, id))
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, errs.ErrIncidentNotFound
		}
		return nil, errs.Wrapf(errs.KindInternal, op, err, "select incident")
	}

	return inc, nil
}

// GetActiveByChatID returns the chat's active incident,
// or errs.ErrNoActiveIncident if the chat has no active incident.
func (r *IncidentRepository) GetActiveByChatID(ctx context.Context, chatID int64) (*incident.Incident, error) {
	const op = "repository.Incident.GetActiveByChatID"
	const query = `
		SELECT id, title, severity, status, chat_id, topic_id, created_by, created_at, closed_at
		FROM incidents
		WHERE chat_id = $1 AND status = $2`

	inc, err := scanIncident(r.db.QueryRow(ctx, query, chatID, incident.StatusActive))
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, errs.ErrNoActiveIncident
		}
		return nil, errs.Wrapf(errs.KindInternal, op, err, "select incident")
	}

	return inc, nil
}

